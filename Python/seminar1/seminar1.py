# За день машина проезжает n километров. Сколько дней нужно, чтобы проехать маршрут длиной m километров?
# При решении этой задачи нельзя пользоваться условной инструкцией if и циклами.

a = int(input('Введите пройденное за день расстояние: '))
b = int(input('Введите длинну маршрута: '))

print("Количество дней для того чтобы пройти маршрут равно ", b//a +(b%a!=0))
print("Количество дней для того чтобы пройти маршрут равно ", (-(-b//a)))
print("Количество дней для того чтобы пройти маршрут равно ", (abs(-b//a)))
print("Количество дней для того чтобы пройти маршрут равно ", (a+b-1)//a)

